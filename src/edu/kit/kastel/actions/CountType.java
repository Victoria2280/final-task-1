package edu.kit.kastel.actions;
/**
 * The CountType enum represents the type of count used in an action.
 * It can either be a fixed value or a randomly generated number.
 * @author uljyv
 * @version 1.0
 */
public enum CountType {
    /** A fixed numerical value. */
    VALUE,
    /** A randomly generated value within a specified range. */
    RANDOM
}